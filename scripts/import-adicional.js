function parseValor(valorStr) {
    if (!valorStr || valorStr === '0') return 0;
    return parseFloat(valorStr.replace(/\./g, '').replace(',', '.')) || 0;
}

function parseData(dataStr) {
    const [dia, mes, ano] = dataStr.split('/');
    return { mes: parseInt(mes), ano: parseInt(ano) };
}

const dbPath = path.join(__dirname, '..', 'data', 'metas.db');
const db = new Database(dbPath);

console.log('🚀 Importando dados adicionais (Jun-Nov 2025)...\n');

const insertStmt = db.prepare(`
    INSERT OR REPLACE INTO metas (unidade_codigo, mes, ano, tipo_produto, meta)
    VALUES (?, ?, ?, ?, ?)
`);

const linhas = dadosAdicionais.split('\n').filter(l => l.trim());
let contador = 0;

const transaction = db.transaction(() => {
    for (const linha of linhas) {
        const partes = linha.split('\t');
        if (partes.length < 4) continue;

        const [unidade, dataStr, tipoProduto, valorStr] = partes;
        const { mes, ano } = parseData(dataStr.trim());
        const valor = parseValor(valorStr.trim());
        const codigo = unidade.trim().padStart(3, '0');

        insertStmt.run(codigo, mes, ano, tipoProduto.trim(), valor);
        contador++;
    }
});

transaction();

console.log(`✅ Importados: ${contador} registros`);

const total = db.prepare('SELECT COUNT(*) as total FROM metas').get();
const periodos = db.prepare('SELECT DISTINCT ano, mes FROM metas ORDER BY ano, mes').all();

console.log(`📊 Total no banco: ${total.total} registros`);
console.log(`📅 Períodos: ${periodos.map(p => `${p.mes}/${p.ano}`).join(', ')}`);

db.close();
console.log('\n🎉 Pronto!');
